const portfolioGrid = document.querySelector("#portfolio-grid");
const filterButtons = document.querySelectorAll(".filter-button");
const menuToggle = document.querySelector(".menu-toggle");
const navLinks = document.querySelector(".nav-links");
const backToTop = document.querySelector(".back-to-top");
const loader = document.querySelector(".page-loader");
const counters = document.querySelectorAll(".counter");
const skillBars = document.querySelectorAll(".skill-bar span");
const revealItems = document.querySelectorAll(".reveal");
const lightbox = document.querySelector("#lightbox");
const lightboxImage = document.querySelector("#lightbox-image");
const lightboxTitle = document.querySelector("#lightbox-title");
const lightboxTags = document.querySelector("#lightbox-tags");
const lightboxClose = document.querySelector(".lightbox-close");
const testimonialCards = document.querySelectorAll(".testimonial-card");
const testimonialButtons = document.querySelectorAll(".testimonial-button");
const contactForm = document.querySelector("#contact-form");
const formNote = document.querySelector("#form-note");

let activeFilter = "all";
let testimonialIndex = 0;
let countersStarted = false;
let skillsStarted = false;

const renderPortfolio = () => {
  portfolioGrid.innerHTML = portfolioItems.map((item) => `
    <article class="portfolio-item" data-tags="${item.tags.join(" ")}">
      <button class="portfolio-media" type="button" aria-label="Open ${item.title} preview">
        <img src="${item.image}" alt="${item.title} logo design by Shantanu Dey" loading="lazy">
        <div class="portfolio-overlay">
          <h3>${item.title}</h3>
          <p>${item.description}</p>
          <div class="portfolio-tags">
            ${item.tags.map((tag) => `<span>${formatTag(tag)}</span>`).join("")}
          </div>
        </div>
      </button>
    </article>
  `).join("");
};

const formatTag = (tag) => {
  return tag
    .replace(/-/g, " ")
    .replace(/\b\w/g, (match) => match.toUpperCase());
};

const applyFilter = (filter) => {
  activeFilter = filter;

  document.querySelectorAll(".portfolio-item").forEach((item) => {
    const tags = item.dataset.tags.split(" ");
    const visible = filter === "all" || tags.includes(filter);
    item.classList.toggle("is-hidden", !visible);
  });
};

const openLightbox = (item) => {
  lightboxImage.src = item.image;
  lightboxImage.alt = `${item.title} logo design enlarged preview`;
  lightboxTitle.textContent = item.title;
  lightboxTags.textContent = item.tags.map(formatTag).join(" | ");
  lightbox.showModal();
};

const animateCounters = () => {
  if (countersStarted) return;
  countersStarted = true;

  counters.forEach((counter) => {
    const target = Number(counter.dataset.target);
    let value = 0;
    const step = Math.max(1, Math.ceil(target / 60));

    const timer = window.setInterval(() => {
      value += step;

      if (value >= target) {
        counter.textContent = target;
        window.clearInterval(timer);
        return;
      }

      counter.textContent = value;
    }, 24);
  });
};

const animateSkills = () => {
  if (skillsStarted) return;
  skillsStarted = true;

  skillBars.forEach((bar) => {
    bar.style.width = `${bar.dataset.progress}%`;
  });
};

const showTestimonial = (index) => {
  testimonialCards.forEach((card, cardIndex) => {
    card.classList.toggle("active", cardIndex === index);
  });
};

const hideLoader = () => {
  loader?.classList.add("hidden");
};

renderPortfolio();
applyFilter(activeFilter);

portfolioGrid.addEventListener("click", (event) => {
  const mediaButton = event.target.closest(".portfolio-media");
  if (!mediaButton) return;

  const itemElement = mediaButton.closest(".portfolio-item");
  const imageSrc = itemElement.querySelector("img").getAttribute("src");
  const item = portfolioItems.find((entry) => entry.image === imageSrc);

  if (item) {
    openLightbox(item);
  }
});

filterButtons.forEach((button) => {
  button.addEventListener("click", () => {
    filterButtons.forEach((item) => item.classList.remove("active"));
    button.classList.add("active");
    applyFilter(button.dataset.filter);
  });
});

menuToggle?.addEventListener("click", () => {
  const expanded = menuToggle.getAttribute("aria-expanded") === "true";
  menuToggle.setAttribute("aria-expanded", String(!expanded));
  navLinks.classList.toggle("open");
});

navLinks?.querySelectorAll("a").forEach((link) => {
  link.addEventListener("click", () => {
    navLinks.classList.remove("open");
    menuToggle?.setAttribute("aria-expanded", "false");
  });
});

const revealObserver = new IntersectionObserver((entries) => {
  entries.forEach((entry) => {
    if (!entry.isIntersecting) return;
    entry.target.classList.add("in-view");

    if (entry.target.closest("#about")) {
      animateCounters();
    }

    if (entry.target.closest("#skills")) {
      animateSkills();
    }
  });
}, { threshold: 0.18 });

revealItems.forEach((item) => revealObserver.observe(item));

window.addEventListener("scroll", () => {
  backToTop.classList.toggle("visible", window.scrollY > 520);
});

backToTop.addEventListener("click", () => {
  window.scrollTo({ top: 0, behavior: "smooth" });
});

lightboxClose.addEventListener("click", () => lightbox.close());
lightbox.addEventListener("click", (event) => {
  const dialogDimensions = lightbox.getBoundingClientRect();
  const clickedOutside =
    event.clientX < dialogDimensions.left ||
    event.clientX > dialogDimensions.right ||
    event.clientY < dialogDimensions.top ||
    event.clientY > dialogDimensions.bottom;

  if (clickedOutside) {
    lightbox.close();
  }
});

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape" && lightbox.open) {
    lightbox.close();
  }
});

testimonialButtons.forEach((button) => {
  button.addEventListener("click", () => {
    testimonialIndex += button.dataset.direction === "next" ? 1 : -1;

    if (testimonialIndex < 0) {
      testimonialIndex = testimonialCards.length - 1;
    }

    if (testimonialIndex >= testimonialCards.length) {
      testimonialIndex = 0;
    }

    showTestimonial(testimonialIndex);
  });
});

window.setInterval(() => {
  testimonialIndex = (testimonialIndex + 1) % testimonialCards.length;
  showTestimonial(testimonialIndex);
}, 5500);

contactForm?.addEventListener("submit", (event) => {
  event.preventDefault();
  const name = contactForm.elements.namedItem("name").value.trim();
  formNote.textContent = `Thanks, ${name || "there"} - your project inquiry is ready. Connect this form to your email or backend endpoint to receive live submissions.`;
  contactForm.reset();
});

window.addEventListener("DOMContentLoaded", () => {
  window.setTimeout(hideLoader, 900);
});

window.addEventListener("load", () => {
  window.setTimeout(hideLoader, 250);
});
